    public void updateFields() {
        Thread updater = new Thread(() -> {
            while(true) {
                try {
                    Thread.sleep(500);

                    String newCompletedProcessesField = String.valueOf(this.stats.completedProcesses) + " de " + String.valueOf(this.stats.totalProcesses);
                    String newAvgWaitField = Double.isNaN(this.stats.avgWait) ? "0.00" : String.format("%.2f", this.stats.avgWait);
                    String newAvgRespField = Double.isNaN(this.stats.avgResp) ? "0.00" : String.format("%.2f", this.stats.avgResp);
                    String newAvgTurnField = Double.isNaN(this.stats.avgTurn) ? "0.00" : String.format("%.2f", this.stats.avgTurn);
                    String newCpuUsageField = String.format("%.1f", this.stats.util) + "%";
                    String newThroughputField = Double.isNaN(this.stats.throughput) ? "0.0000" : String.format("%.4f", this.stats.throughput);
                    String newFairnessField = Double.isNaN(this.stats.fairness) ? "0.000" : String.format("%.3f", this.stats.fairness);
                    
                    String newCurrentField = this.stats.isCurrentProcessAvailable() ? this.stats.currentProcess.name() : "Ninguno";
                    String[] newReadyList = this.stats.getReadyQueueList();
                    String[] newIoList = this.stats.getIoQueueList();

                    this.completedProcessesField.setText(newCompletedProcessesField);
                    this.avgWaitField.setText(newAvgWaitField);
                    this.avgRespField.setText(newAvgRespField);
                    this.avgTurnField.setText(newAvgTurnField);
                    this.cpuUsageField.setText(newCpuUsageField);
                    this.throughputField.setText(newThroughputField);
                    this.fairnessField.setText(newFairnessField);
                    
                    this.currentProcessField.setText(newCurrentField);
                    this.ReadyList.setListData(newReadyList);
                    this.BlockedList.setListData(newIoList);
                } catch (InterruptedException e) {
                     Thread.currentThread().interrupt();
                }
            }
        }, "External-Updater");

       
        updater.start();
    }

