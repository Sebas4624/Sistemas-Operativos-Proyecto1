    public void updateConfig() {
        String amount = this.cyclesAmountField.getText();
        String duration = this.cyclesDurationField.getText();
        String quantum = this.cyclesQuantumField.getText();
        
        try {
            int amountParsed = Integer.parseInt(amount);
            this.sim.setCyclesAmount(amountParsed);
        } catch (NumberFormatException e) {
        }
        
        try {
            long durationParsed = Long.parseLong(duration);
            this.sim.setCyclesDuration(durationParsed);
        } catch (NumberFormatException e) {
        }
        
        try {
            int quantumParsed = Integer.parseInt(quantum);
            this.sim.setCyclesQuantum(quantumParsed);
        } catch (NumberFormatException e) {
        }
    }
    
    public void loadConfig() {
        this.cyclesAmountField.setText(String.valueOf(this.sim.getCyclesAmount()));
        this.cyclesDurationField.setText(String.valueOf(this.sim.getCyclesDuration()));
        this.cyclesQuantumField.setText(String.valueOf(this.sim.getCyclesQuantum()));
    }
    
